var sorteados = [];
var attempts = 0;

function sortear() {
    Math.random();
    attempts = 0;

    var vencedorElement = $("#vencedor");
    vencedorElement.text("[sorteando...]");

    var participantes = $("#participantes").val().trim();

    if(participantes.length < 1)
        return;

    var lines = participantes.match(/[^\r\n]+/g);
    var i = getRandomIndex(lines.length);
    var result = lines[i];

    vencedorElement.text(result);
}

function getRandomIndex(max) {
    attempts++;
    var i = Math.floor((Math.random() * max));

    if(sorteados.indexOf(i) >= 0 && attempts <= 5)
        i = getRandomIndex(max);

    sorteados.push(i);
    return i;
}